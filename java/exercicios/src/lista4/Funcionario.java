package lista4;

public class Funcionario {

	public String nome;
	public String cadastro;
	public String chefe;
	public String setor;
	public double salario;
	
	public Funcionario(String nome, String cadastro, String chefe, String setor, double salario) {
		this.nome = nome;
		this.cadastro = cadastro;
		this.chefe = chefe;
		this.setor = setor;
		this.salario = salario;
	}
}
