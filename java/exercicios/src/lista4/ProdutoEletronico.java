package lista4;

public class ProdutoEletronico {

	public String tipo;
	public String marca;
	public String cor;
	public double consumo;
	
	public ProdutoEletronico(String tipo, String marca, String cor, double consumo) {
		this.tipo = tipo;
		this.marca = marca;
		this.cor = cor;
		this.consumo = consumo;
	}
}
