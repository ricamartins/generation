package lista4;

public class Paciente {

	public String nome;
	public String convenio;
	public String quarto;
	public boolean alta;
	
	public Paciente(String nome, String convenio, String quarto, boolean alta) {
		this.nome = nome;
		this.convenio = convenio;
		this.quarto = quarto;
		this.alta = alta;
	}
}
