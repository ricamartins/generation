package lista4;

public class Patinete {

	public String cor;
	public String marca;
	public double altura;
	public double largura;

	public Patinete(String marca, String cor, double altura, double largura) {
		this.marca = marca;
		this.cor = cor;
		this.altura = altura;
		this.largura = largura;
	}
}
