package aula;

public class Aluno {

	public String nome;
	public String matricula;
	public String curso;
	public int anoNascimento;
	public double notaGeral;
	
	public Aluno(String nome, String matricula, String curso, int anoNascimento, double notaGeral) {
		this.nome = nome;
		this.matricula = matricula;
		this.curso = curso;
		this.anoNascimento = anoNascimento;
		this.notaGeral = notaGeral;
	}
}

